fun main(){
    println("hello world")


    var a:Int=7
    println("$a:integer variable")

    var b:Float=1.43f
    println("$b:float variable")

    var c:String="kotlin"
    println("$c:string variable")

    var d:Double=1.234
    println("$d: Double variable")

    var e:Boolean=true
    println("$e: Boolean variable")

    var f:Char='a'
    println("$f: Character variable")

    var l:Long=21012011111
    println("$l: long value")

    var s:Short=43
    println("$s: short variable")

    var g:Byte=127
    println("$g:Byte variable")

    val x=3.14
    println("$x: constant value")
}